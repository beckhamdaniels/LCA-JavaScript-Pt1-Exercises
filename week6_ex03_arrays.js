let colors = [] //Created an empty array called colors//

colors.push("red")  // Added 3 new colours using push method//
colors.push("green")
colors.push("blue")

let numbers = [1, 2, 3, 4, 5] //created a new array with 5 numbers//

colors.pop() //Removed the last element of the colors array using pop method//

colors.unshift("yellow") //Added a new colour to the beginning of the colors array using unshift method//

console.log(`The length of colors is: ${colors.length}, The length of numbers is: ${numbers.length}`) //Printed the length of both arrays using length property//
